# ─── API функция ───────────────────────────────────────────────────────────────

def get_pair_meaning(planet_a: str, planet_b: str, aspect_type: str) -> str | None:
    """
    Return atomic meaning for a planet pair + aspect type.
    Tries both orderings (a,b) and (b,a).
    Returns None if pair not in dictionary.
    """
    category = aspect_category(aspect_type)
    if category == "minor":
        return None

    for pa, pb in [(planet_a, planet_b), (planet_b, planet_a)]:
        meaning = ASPECT_PAIRS.get((pa, pb, category))
        if meaning:
            return meaning
    return None


