def aspect_category(aspect_type: str) -> str:
    """Map aspect type → category key used in ASPECT_PAIRS."""
    t = aspect_type.lower()
    if t in ("square", "opposition"):
        return "tense"
    if t in ("trine", "sextile"):
        return "harmonious"
    if t == "conjunction":
        return "conjunction"
    return "minor"  # quincunx, semisquare, etc. — no entry needed


