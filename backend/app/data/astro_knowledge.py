# ─── ACCESSOR FUNCTIONS ───────────────────────────────────────────────────────

def get_planet_kb(planet_name: str) -> dict:
    return PLANETS_KB.get(planet_name, {})


def get_sign_kb(sign_name: str) -> dict:
    return SIGNS_KB.get(sign_name, {})


def get_house_kb(house_num: int) -> dict:
    return HOUSES_KB.get(str(house_num), {})


def get_aspect_kb(aspect_type: str) -> dict:
    return ASPECTS_KB.get(aspect_type, {})


def get_dignity_kb(dignity_score: int) -> dict:
    if dignity_score >= 5:
        return DIGNITIES_KB["domicile"]
    elif dignity_score == 4:
        return DIGNITIES_KB["exaltation"]
    elif dignity_score == -5:
        return DIGNITIES_KB["detriment"]
    elif dignity_score == -4:
        return DIGNITIES_KB["fall"]
    return DIGNITIES_KB["neutral"]


def get_sphere_knowledge(sphere_ctx: dict) -> dict:
    """
    Extract a focused knowledge bundle for one sphere.
    Called by the sphere worker agent before building its prompt.

    Returns only what's relevant to this sphere — no data pollution.
    """
    sphere_num = sphere_ctx["sphere"]
    cusp_sign  = sphere_ctx.get("cusp_sign", "")
    ruler      = sphere_ctx.get("ruler") or {}
    co_ruler   = sphere_ctx.get("co_ruler") or {}

    # House knowledge
    house_kb = get_house_kb(sphere_num)

    # Cusp sign knowledge
    sign_kb = get_sign_kb(cusp_sign)

    # Ruler knowledge
    ruler_kb = get_planet_kb(ruler.get("name", "")) if ruler else {}

    # Ruler sign knowledge
    ruler_sign_kb = get_sign_kb(ruler.get("sign", "")) if ruler else {}

    # Ruler dignity
    ruler_dignity_kb = get_dignity_kb(ruler.get("dignity_score", 0)) if ruler else {}

    # Co-ruler knowledge
    co_ruler_kb = get_planet_kb(co_ruler.get("name", "")) if co_ruler else {}

    # Aspect knowledge for top aspects to ruler
    aspects_kb = {}
    for asp in sphere_ctx.get("aspects_to_ruler", [])[:5]:
        asp_type = asp.get("type", "")
        if asp_type not in aspects_kb:
            aspects_kb[asp_type] = get_aspect_kb(asp_type)
        # Add partner planet knowledge
        for key in ["planet_a", "planet_b"]:
            pname = asp.get(key, "")
            if pname and pname != ruler.get("name"):
                if pname not in aspects_kb:
                    aspects_kb[pname + "_planet"] = get_planet_kb(pname)

    # Planets in house knowledge
    residents_kb = {}
    for p in sphere_ctx.get("planets_in_house", []):
        pname = p.get("name", "")
        if pname:
            residents_kb[pname] = {
                "planet": get_planet_kb(pname),
                "sign":   get_sign_kb(p.get("sign", "")),
                "dignity": get_dignity_kb(p.get("dignity_score", 0)),
            }

    # Balance element/modality
    balance = sphere_ctx.get("balance", {})
    dom_el  = balance.get("dominant_element")
    dom_mod = balance.get("dominant_modality")
    element_kb   = ELEMENTS_KB.get(dom_el, {}) if dom_el else {}
    modality_kb  = MODALITIES_KB.get(dom_mod, {}) if dom_mod else {}

    # Ruler planet-in-sign interpretation
    ruler_in_sign_kb = {}
    if ruler:
        rname = ruler.get("name", "")
        rsign = ruler.get("sign", "")
        ruler_in_sign_kb = PLANET_IN_SIGN.get(rname, {}).get(rsign, {})

    # Ruler planet-in-house interpretation
    ruler_in_house_kb = {}
    if ruler:
        rname = ruler.get("name", "")
        rhouse = str(ruler.get("house", ""))
        ruler_in_house_kb = PLANET_IN_HOUSE.get(rname, {}).get(rhouse, {})

    # Resident planets in sign and house
    residents_in_sign_kb = {}
    residents_in_house_kb = {}
    for p in sphere_ctx.get("planets_in_house", []):
        pname = p.get("name", "")
        psign = p.get("sign", "")
        phouse = str(sphere_num)
        residents_in_sign_kb[pname]  = PLANET_IN_SIGN.get(pname, {}).get(psign, {})
        residents_in_house_kb[pname] = PLANET_IN_HOUSE.get(pname, {}).get(phouse, {})

    # Planetary pairs involving ruler
    pairs_kb = {}
    if ruler:
        rname = ruler.get("name", "")
        for asp in sphere_ctx.get("aspects_to_ruler", [])[:5]:
            other = asp["planet_b"] if asp["planet_a"] == rname else asp["planet_a"]
            pair_key_a = f"{rname}_{other}"
            pair_key_b = f"{other}_{rname}"
            pair_data = PLANETARY_PAIRS_KB.get(pair_key_a) or PLANETARY_PAIRS_KB.get(pair_key_b)
            if pair_data:
                pairs_kb[pair_key_a] = pair_data

    return {
        "house":              house_kb,
        "cusp_sign":          sign_kb,
        "ruler":              ruler_kb,
        "ruler_sign":         ruler_sign_kb,
        "ruler_dignity":      ruler_dignity_kb,
        "ruler_in_sign":      ruler_in_sign_kb,
        "ruler_in_house":     ruler_in_house_kb,
        "co_ruler":           co_ruler_kb,
        "aspects":            aspects_kb,
        "residents":          residents_kb,
        "residents_in_sign":  residents_in_sign_kb,
        "residents_in_house": residents_in_house_kb,
        "planetary_pairs":    pairs_kb,
        "critical_degrees":   CRITICAL_DEGREES_KB,
        "dominant_element":   element_kb,
        "dominant_modality":  modality_kb,
    }
